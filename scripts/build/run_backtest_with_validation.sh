#!/usr/bin/env bash
set -euo pipefail

script_dir="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
repo_root="$(cd "${script_dir}/../.." && pwd)"
cd "${repo_root}"

config_path="configs/ops/backtest_run.yaml"
fast_mode=false
fast_start_date=""
fast_end_date=""
fast_max_ticks=""
forward_args=()
temp_config=""

trim() {
  local value="$1"
  value="${value#"${value%%[![:space:]]*}"}"
  value="${value%"${value##*[![:space:]]}"}"
  printf '%s' "${value}"
}

unquote() {
  local value="$1"
  if [[ ${#value} -ge 2 ]]; then
    if [[ "${value:0:1}" == '"' && "${value: -1}" == '"' ]]; then
      value="${value:1:${#value}-2}"
    elif [[ "${value:0:1}" == "'" && "${value: -1}" == "'" ]]; then
      value="${value:1:${#value}-2}"
    fi
  fi
  printf '%s' "${value}"
}

to_abs_path() {
  local value="$1"
  if [[ -z "${value}" ]]; then
    return 0
  fi
  if [[ "${value}" = /* ]]; then
    printf '%s' "${value}"
  else
    printf '%s' "${repo_root}/${value}"
  fi
}

resolve_runs_root_from_config() {
  local path="$1"
  local output_root_dir=""
  local line=""

  if [[ -f "${path}" ]]; then
    while IFS= read -r line || [[ -n "${line}" ]]; do
      line="${line%%#*}"
      if [[ "${line}" =~ ^[[:space:]]*output_root_dir[[:space:]]*:(.*)$ ]]; then
        output_root_dir="${BASH_REMATCH[1]}"
      fi
    done <"${path}"
  fi

  output_root_dir="$(trim "$(unquote "${output_root_dir}")")"
  if [[ -z "${output_root_dir}" ]]; then
    output_root_dir="docs/results/backtest_runs"
  fi
  to_abs_path "${output_root_dir}"
}

usage() {
  cat <<'EOF'
Usage: scripts/build/run_backtest_with_validation.sh [options]

Options:
  --config PATH            Backtest run config file (default: configs/ops/backtest_run.yaml)
  --fast                   Disable heavy trace outputs and formal report generation for faster iteration
  --fast-start-date DATE   Override start_date in fast mode (YYYYMMDD)
  --fast-end-date DATE     Override end_date in fast mode (YYYYMMDD)
  --fast-max-ticks N       Override max_ticks in fast mode
  --skip-build             Forwarded to run_backtest_from_config.sh
  --dry-run                Forwarded to run_backtest_from_config.sh
  -h, --help               Show help
EOF
}

cleanup() {
  if [[ -n "${temp_config}" && -f "${temp_config}" ]]; then
    rm -f "${temp_config}"
  fi
}

trap cleanup EXIT

while [[ $# -gt 0 ]]; do
  case "$1" in
    --config)
      if [[ $# -lt 2 ]]; then
        echo "error: --config requires a value" >&2
        usage >&2
        exit 2
      fi
      config_path="$2"
      shift 2
      ;;
    --fast)
      fast_mode=true
      shift
      ;;
    --fast-start-date)
      if [[ $# -lt 2 ]]; then
        echo "error: --fast-start-date requires a value" >&2
        usage >&2
        exit 2
      fi
      fast_start_date="$2"
      shift 2
      ;;
    --fast-end-date)
      if [[ $# -lt 2 ]]; then
        echo "error: --fast-end-date requires a value" >&2
        usage >&2
        exit 2
      fi
      fast_end_date="$2"
      shift 2
      ;;
    --fast-max-ticks)
      if [[ $# -lt 2 ]]; then
        echo "error: --fast-max-ticks requires a value" >&2
        usage >&2
        exit 2
      fi
      fast_max_ticks="$2"
      shift 2
      ;;
    -h|--help)
      usage
      exit 0
      ;;
    *)
      forward_args+=("$1")
      shift
      ;;
  esac
done

if [[ "${fast_mode}" != true ]] && [[ -n "${fast_start_date}${fast_end_date}${fast_max_ticks}" ]]; then
  echo "error: --fast-start-date/--fast-end-date/--fast-max-ticks require --fast" >&2
  exit 2
fi

if [[ -n "${fast_start_date}" && ! "${fast_start_date}" =~ ^[0-9]{8}$ ]]; then
  echo "error: --fast-start-date must be YYYYMMDD (got: ${fast_start_date})" >&2
  exit 2
fi

if [[ -n "${fast_end_date}" && ! "${fast_end_date}" =~ ^[0-9]{8}$ ]]; then
  echo "error: --fast-end-date must be YYYYMMDD (got: ${fast_end_date})" >&2
  exit 2
fi

if [[ -n "${fast_max_ticks}" && ! "${fast_max_ticks}" =~ ^[1-9][0-9]*$ ]]; then
  echo "error: --fast-max-ticks must be a positive integer (got: ${fast_max_ticks})" >&2
  exit 2
fi

if [[ "${fast_mode}" == true ]]; then
  if [[ ! -f "${config_path}" ]]; then
    echo "error: config file not found: ${config_path}" >&2
    exit 2
  fi

  temp_config="$(mktemp "${TMPDIR:-/tmp}/quant_hft_backtest_fast_XXXXXX.yaml")"
  cp "${config_path}" "${temp_config}"
  {
    printf '\n# fast mode overrides generated by run_backtest_with_validation.sh\n'
    printf 'emit_indicator_trace: false\n'
    printf 'emit_sub_strategy_indicator_trace: false\n'
    printf 'emit_formal_analysis_report: false\n'
    if [[ -n "${fast_start_date}" ]]; then
      printf 'start_date: %s\n' "${fast_start_date}"
    fi
    if [[ -n "${fast_end_date}" ]]; then
      printf 'end_date: %s\n' "${fast_end_date}"
    fi
    if [[ -n "${fast_max_ticks}" ]]; then
      printf 'max_ticks: %s\n' "${fast_max_ticks}"
    fi
  } >>"${temp_config}"

  config_path="${temp_config}"
  echo "[step] fast 模式已启用：关闭 indicator/sub-strategy trace 与正式分析报告"
  if [[ -n "${fast_start_date}" || -n "${fast_end_date}" || -n "${fast_max_ticks}" ]]; then
    echo "[step] fast 覆盖：start_date=${fast_start_date:-<unchanged>} end_date=${fast_end_date:-<unchanged>} max_ticks=${fast_max_ticks:-<unchanged>}"
  fi
fi

runs_root="$(resolve_runs_root_from_config "${config_path}")"
mkdir -p "${runs_root}"

latest_before="$(ls -1dt "${runs_root}"/backtest-* 2>/dev/null | head -n 1 || true)"

echo "[step] 执行回测"
"${script_dir}/run_backtest_from_config.sh" --config "${config_path}" "${forward_args[@]}"

latest_after="$(ls -1dt "${runs_root}"/backtest-* 2>/dev/null | head -n 1 || true)"
if [[ -z "${latest_after}" ]]; then
  echo "error: 未找到回测输出目录: ${runs_root}" >&2
  exit 2
fi

if [[ -n "${latest_before}" && "${latest_before}" == "${latest_after}" ]]; then
  echo "[warn] 未检测到新回测目录，默认对最新目录做检测: ${latest_after}"
else
  echo "[step] 检测目录: ${latest_after}"
fi

echo "[step] 生成检测报告"
python3 "${repo_root}/scripts/analysis/backtest_validation_report.py" --run-dir "${latest_after}" --strict

formal_report="$(find "${latest_after}" -maxdepth 1 -type f -name '*_回测分析报告_*.md' | sort | tail -n 1 || true)"

if [[ -n "${formal_report}" ]]; then
  echo "[done] 检测完成，报告路径: ${latest_after}/validation_report.md；正式分析报告: ${formal_report}"
else
  echo "[done] 检测完成，报告路径: ${latest_after}/validation_report.md"
fi
